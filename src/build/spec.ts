// Pure helpers to turn a Claude-generated implementation plan into a structured
// "build spec" + a prompt that hands it to Claude Code's harness. Obsidian-free
// so it can be unit-tested.

import { stripTags } from "../artifacts/parse";

export interface BuildTask {
  title: string;
  done: boolean;
}

/**
 * Extract a checklist of tasks from a plan. Recognizes Markdown checkboxes
 * ("- [ ] x" / "- [x] x") and, failing that, milestone-like list items.
 */
export function extractTasks(planText: string): BuildTask[] {
  const tasks: BuildTask[] = [];
  const checkbox = /^\s*[-*]\s*\[( |x|X)\]\s+(.*\S)/;
  for (const line of planText.split("\n")) {
    const m = checkbox.exec(line);
    if (m) tasks.push({ title: m[2].trim(), done: m[1].toLowerCase() === "x" });
  }
  if (tasks.length > 0) return tasks;

  // Fallback: numbered or bulleted milestone lines (strip HTML tags first —
  // iterate-until-stable so multi-char reconstruction can't smuggle a tag through).
  const text = stripTags(planText, " ");
  const item = /^\s*(?:\d+[.)]|[-*•])\s+(.*\S)/;
  for (const line of text.split("\n")) {
    const m = item.exec(line);
    if (m && m[1].length > 3) tasks.push({ title: m[1].trim().slice(0, 140), done: false });
  }
  return tasks.slice(0, 40);
}

export interface SpecInput {
  title: string;
  /** The plan content (markdown or the artifact's text). */
  plan: string;
  /** Vault path of the tracker note Claude Code should update. */
  trackerPath: string;
  /** Vault path of the spec note itself. */
  specPath: string;
  /** Optional vault name (kept for callers; the MCP bridge targets one vault). */
  vault?: string;
  tasks: BuildTask[];
}

/** Build the structured spec note body (frontmatter is added by the caller). */
export function specBody(input: SpecInput): string {
  const lines: string[] = [];
  lines.push(`# Build spec: ${input.title}`, "");
  lines.push("> Generated by Claude Companion for handoff to Claude Code.", "");
  lines.push("## Tasks", "");
  for (const t of input.tasks) lines.push(`- [${t.done ? "x" : " "}] ${t.title}`);
  lines.push("", "## Plan", "", input.plan.trim(), "");
  return lines.join("\n");
}

/**
 * The prompt to paste into Claude Code. The vault is exposed to Claude Code as
 * an MCP server (Companion for Claude, pre-wired in the plugin's .mcp.json), so
 * the build is driven through the `note_read` / `note_append` MCP tools — not a
 * separate CLI.
 */
export function buildPrompt(input: SpecInput): string {
  return [
    `This Obsidian vault is exposed to you as an MCP server (Companion for Claude).`,
    `Drive this build through its tools — do not shell out to any CLI.`,
    ``,
    `1. Read the build spec with the \`note_read\` tool:  path = "${input.specPath}"`,
    `2. Implement the tasks in order. Keep changes focused and runnable.`,
    `3. After each task, record progress with the \`note_append\` tool:`,
    `   path = "${input.trackerPath}", content = "- [x] <task> — <one-line note> (<timestamp>)"`,
    `4. If a task is blocked, append "- [ ] <task> — BLOCKED: <reason>" instead and continue.`,
    `5. When all tasks are done, append a "## Summary" section to the tracker the same way.`,
    `   (Writes require Companion's "Allow writes" MCP setting to be on.)`,
    ``,
    `Build spec title: ${input.title}`,
  ].join("\n");
}

/** The `claude` CLI invocation that starts the build in a terminal. */
export function claudeCodeBuildCommand(input: SpecInput): string {
  // Escape backslashes before quotes so literal backslashes cannot interfere
  // with the quote escaping for the shell's double-quoted -p argument.
  const prompt = buildPrompt(input)
    .replace(/\\/g, "\\\\")
    .replace(/"/g, '\\"')
    .replace(/\$/g, "\\$")
    .replace(/`/g, "\\`");
  return `claude -p "${prompt}"`;
}
